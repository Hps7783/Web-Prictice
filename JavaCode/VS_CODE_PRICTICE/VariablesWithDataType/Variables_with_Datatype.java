/*Show how to mathematical calculation using variables like Addition, 
Subtraction, Multiplication, Division and modulas are implemanted*/

package VariablesWithDataType;

public class Variables_with_Datatype {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = 30;
        int d = 40;
        int e = 50;
        int f = 60;
        int g = 70;
        int h = 80;
        int i = 90;
        int j = 100;

        System.out.println("Addition of two numbers is : " + (a+b));
        System.out.println("Subtraction of two numbers is : " + (c-d));
        System.out.println("Multiplication of two numbers is : " + (e*f));
        System.out.println("Division of two numbers is : " + (g/h));
        System.out.println("Modulus of two numbers is : " + (i%j));
        
    }  
}
