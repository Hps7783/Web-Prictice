package JavaCode.VS_CODE_PRICTICE.Operator.Operators;

public class AssignmentOperator {
    public static void main(String[] args) {
        int a=10;
        int b=20;
        
        a=b;
         System.out.println(a);     
        a+=b;
         System.out.println(a);     
        a-=b;
        System.out.println(a);
        a*=b;
        System.out.println(a);
        a/=b;
        System.out.println(a);
        
    }
    
}
